# There are two types of  operators in Python
# 1. Membership Operator:
#     1. in   2. not in
# example 
name="Yugal"
print('Z' in name) # False( Z is not in Yugal)
print('Z' not in name)  # True

for i in range(1,6):       
    print(i)

for i in range(5,-1,-1):  #Reverse       
    print(i)

print("HII")

